# 元组
# TODO 类似java数组但又不是数组 ，但是时间复杂度是围绕O(1)左右的
# 元组可以用来存储多个值，但是元组的元素不能被修改、删除或新增。
my_tuple = (1, 2, 3, 'a', 'b', 'c')
print(my_tuple[0])  # 输出结果为 1
print(my_tuple[3])  # 输出结果为 'a'
print(my_tuple[2:5])  # 输出结果为 (3, 'a', 'b')
print(len(my_tuple))  # 输出结果为 6

# TODO 元组的时间复杂度是围绕O(1)左右的，而列表的时间复杂度是围绕O(N)左右的。

